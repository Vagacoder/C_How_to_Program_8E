#include <stdio.h>
#include <stdlib.h>

int main(void) {

	unsigned int a = 1, b =1;

	printf("a is %d\n", a);
	printf("a++ is %d\n", a++);
	printf("now a %d\n", a);

	printf("b is %d\n", b);
	printf("++b is %d\n", ++b);
	printf("now b is %d\n", b);

	return EXIT_SUCCESS;
}
